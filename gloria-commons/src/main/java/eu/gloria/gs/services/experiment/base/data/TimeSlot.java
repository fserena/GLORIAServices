package eu.gloria.gs.services.experiment.base.data;

import java.util.Date;

public class TimeSlot {

	private Date begin;
	private Date end;
	
	public Date getBegin() {
		return begin;
	}

	public void setBegin(Date begin) {
		this.begin = begin;
	}
	
	public Date getEnd() {
		return end;
	}

	public void setEnd(Date end) {
		this.end = end;
	}
}
